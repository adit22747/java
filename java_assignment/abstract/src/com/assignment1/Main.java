package com.assignment1;

abstract class Animal {
	public void respirate() {
		System.out.println("all animals repsirate");
	}

	public abstract void talk();
}

class cat extends Animal {

	public void talk() {
		System.out.println("cat does not speak");

	}

}

class dog extends Animal {

	public void talk() {
		System.out.println("dog does not speak");

	}

}

class lion extends Animal {

	public void talk() {
		System.out.println("lion does not speak");

	}

}

public class Main {
	public static void main(String[] args) {
		Animal cat = new cat();
		cat.respirate();
		cat.talk();
		Animal dog = new dog();
		dog.talk();
		Animal lion = new lion();
		lion.talk();
	}
}